package com.tvntd.models;

import javax.persistence.Embeddable;

@Embeddable
public class UserNotifyItem
{
    private String     title;
    private String     image;
    private Long       milliEpoc;
    private String     subject;
    private String     status;
    private String     message;

    private String     icon;
    private String     taskComplete;

    /**
     * @return the title
     */
    public String getTitle() {
        return title;
    }

    /**
     * @param title the title to set
     */
    public void setTitle(String title) {
        this.title = title;
    }

    /**
     * @return the image
     */
    public String getImage() {
        return image;
    }

    /**
     * @param image the image to set
     */
    public void setImage(String image) {
        this.image = image;
    }

    /**
     * @return the milliEpoc
     */
    public Long getMilliEpoc() {
        return milliEpoc;
    }

    /**
     * @param milliEpoc the milliEpoc to set
     */
    public void setMilliEpoc(Long milliEpoc) {
        this.milliEpoc = milliEpoc;
    }

    /**
     * @return the subject
     */
    public String getSubject() {
        return subject;
    }

    /**
     * @param subject the subject to set
     */
    public void setSubject(String subject) {
        this.subject = subject;
    }

    /**
     * @return the status
     */
    public String getStatus() {
        return status;
    }

    /**
     * @param status the status to set
     */
    public void setStatus(String status) {
        this.status = status;
    }

    /**
     * @return the message
     */
    public String getMessage() {
        return message;
    }

    /**
     * @param message the message to set
     */
    public void setMessage(String message) {
        this.message = message;
    }

    /**
     * @return the icon
     */
    public String getIcon() {
        return icon;
    }

    /**
     * @param icon the icon to set
     */
    public void setIcon(String icon) {
        this.icon = icon;
    }

    /**
     * @return the taskComplete
     */
    public String getTaskComplete() {
        return taskComplete;
    }

    /**
     * @param taskComplete the taskComplete to set
     */
    public void setTaskComplete(String taskComplete) {
        this.taskComplete = taskComplete;
    }
}
